package com.jju.bean;

public class Favorite {
	private int fid;    //收藏id
	private int gid;    //收藏商品id
	private int uname;  //收藏的用户名
	public int getFid() {
		return fid;
	}
	public void setFid(int fid) {
		this.fid = fid;
	}
	public int getGid() {
		return gid;
	}
	public void setGid(int gid) {
		this.gid = gid;
	}
	public int getUname() {
		return uname;
	}
	public void setUname(int uname) {
		this.uname = uname;
	}
	
}
