package com.jju.bean;

public class Category {
	private int cid;   //类别id
	private String cname;  //类别名称
	public int getCid() {
		return cid;
	}
	public void setCid(int cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	
}
